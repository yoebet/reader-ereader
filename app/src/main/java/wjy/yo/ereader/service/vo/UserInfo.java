package wjy.yo.ereader.service.vo;

public class UserInfo {

    private boolean login;

    private String name;

    public boolean isLogin() {
        return login;
    }

    public void setLogin(boolean login) {
        this.login = login;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
